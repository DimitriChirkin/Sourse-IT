public  class Swit {
	private String Name ;
	
	private int Sugar ;
	
	private int Ves ;
	
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}

	public int getVes() {
		return Ves;
	}
	public void setVes(int ves) {
		Ves = ves;
	}
	public int getSugar() {
		return Sugar;
	}
	public void setSugar(int sugar) {
		Sugar = sugar;
	}

	public Swit(String Name , int Sugar , int Ves) {
		this.Name = Name ;
		this.Sugar = Sugar ;
		this.Ves = Ves ;
		
		
	}
	
	

}
